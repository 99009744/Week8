function addition(a, b){
    var c = a+b;
    document.write (a+"+"+b+"="+c+"<br>");
}
addition(12,99);
addition(44,21);
addition(71,31);

function subtraction(d, e){
    var f = d-e;
    document.write (d+"-"+e+"="+f+"<br>");
}
subtraction(42,11);
subtraction(91,67);
subtraction(12,3);

function multiplication(g, h){
    var i = g*h;
    document.write (g+"x"+h+"="+i+"<br>");
}
multiplication(4,11);
multiplication(9,6);
multiplication(5,3);

function division(j, k){
    var l = j*k;
    document.write (j+":"+k+"="+l+"<br>");
}
division(400,10);
division(90,30);
division(6,3);

